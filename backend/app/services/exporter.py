"""Export packaging: prompt + control-signal sequences + masks + metadata → zip.

The zip is the hand-off artifact the director feeds to an external AI video
tool (ComfyUI Wan Fun-Control / VACE / LTX, or a commercial platform).
"""

import json
import logging
import re
import time
import traceback
import zipfile
from datetime import datetime, timezone
from pathlib import Path

import cv2
import numpy as np

from app import __version__
from app.db import SessionLocal
from app.models import BackgroundEdit, ExportRecord, LensState, PromptRecord, Shot
from app.services import lens as lens_service
from app.services import paths
